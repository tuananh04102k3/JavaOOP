public class FirstTwentyPositiveIntegers {
    public static void main(String[] args) {
        // Số lượng số nguyên dương cần in ra
        int count = 20;

        // In ra 20 số nguyên dương đầu tiên
        System.out.println("20 số nguyên dương đầu tiên:");
        for (int i = 1; i <= count; i++) {
            System.out.print(i + " ");
        }
    }
}
